# 01 - Basic Data Types

# Integer
count = 10
print("Integer:", count, type(count))

# Float
price = 19.99
print("Float:", price, type(price))

# String
message = "Hello, Python!"
print("String:", message, type(message))

# Boolean
is_active = True
print("Boolean:", is_active, type(is_active))

# NoneType
not_set = None
print("NoneType:", not_set, type(not_set))
